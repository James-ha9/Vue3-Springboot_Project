        <h1>基于利益再分配的视角</h1>
        <h2>项目进村背景下民族地区乡村善治的实现机制研究</h2>
      </h1>
      <p class="meta-info">2024年1月15日</p>
      <div class="article-content">
        <p>摘要</p>
        <p>
          项目进村是推动民族地区持续稳定发展的有效推手，也是拓展脱贫攻坚成果同乡村振兴有效衔接和推动乡村治理现代化的重要途径。学界关于项目进村中的乡村治理研究，大多关注项目“从公到私”的利益分配格局，而忽视实践操作中利益分配“由私到公”的乡村价值再造过程。项目进村以来，民族地区项目利益分配逐渐私人化，为遏止利益分配失序和失衡，村庄权威精英通过与村干部博弈，而创新利益再分配规则，“逆向治理”模式应运而生。逆向治理以“双层利益再分配结构”为内在逻辑，依赖利益再分配机制，充分发挥村庄精英的协调作用，最终促进民族地区新型乡村治理体系的构建，从而推动乡村善治的形成和治理能力的现代化。
        </p>
        <p>
          党的十九大提出“健全自治、法治、德治相结合的乡村治理体系和治理有效的乡村振兴战略”的重要论断，对推进民族地区构建共建、共治、共享的基层治理格局，促进民族地区和谐稳定、有序发展具有重要指导意义。民族地区的乡村治理不仅要考虑乡村社会发展的共性问题，也要考虑民族、宗教、文化、价值观等个性问题。新时代新发展阶段推动民族地区形成自治、法治、德治“三治融合”的乡村善治模式，就要充分利用好民族地区乡村振兴的政策红利，处理好有效治理与高质量发展、民族传统与时代特色等关系。近年来在分税制改革和产业扶贫政策下，国家加大了项目进村和财政转移性支付的力度，但在民族地区的乡村治理实践中却出现了与国家意图相悖的利益分配失序的不和谐音符。这不仅损害了民族地区乡村社会的公共利益，而且滞阻了民族地区乡村善治的进程。如何在项目进村中，构建一种既能化解现实矛盾，又能推动民族地区乡村振兴的善治模式，是当务之急。
        </p>
        <p>一、文献分析和理论范式</p>
        <p>1、项目进村机制与基层治理变化：</p>
        <p>基层政府“发包”和村庄“抓包”成为项目进村后的重要治理机制。</p>
        <p>
          乡镇由“悬浮型政权”转变为“协调型政权”，村干部成为国家代理人和村庄协调者。
        </p>
        <p>理论上，这种转变有利于协调村庄分化和分利，趋向良善治理。</p>
        <p>2、实践中的利益分配失序：</p>
        <p>
          基层实践中，官僚动员、正式行政和技术治理的片面强调，以及“非正式运作”的控制，导致项目价值被剥离，变为利益分配过程。
        </p>
        <p>“分利型”村干部成长，村庄治理陷入寡头秩序。</p>
        <p>
          村支书等利用私人关系和资源谋取私利，出现“私人化运作”和“公权私用”。
        </p>
        <p>村干部、村庄精英与村民之间出现“赢者通吃”的马太效应。</p>
        <p>3、利益分配失序的原因与对策：</p>
        <p>
          原因包括村庄回应效能低下、基层组织困境、国家资源再分配自主性不足、逐利价值观下的“官商勾结”、权威与技术治理培养的“利维坦”、扶贫资源不精准入户等。
        </p>
        <p>
          对策包括重拾群众路线、村委会班子建设和村干部培育、构建良善互动格局、提高国家资源分配自主性能力等。
        </p>
        <p>4、项目进村中的民族地区乡村治理特殊性：</p>
        <p>民族地区项目进村后的利益分配问题更为复杂，且学界对此关注不够。</p>
        <p>
          民族地区的自然环境、历史文化传统、经济社会发展相对复杂，需根据特殊性和差异性构建治理模式。
        </p>
        <p>5、本文研究的主要问题：</p>
        <p>民族地区项目进村后的利益分配（由公到私）情况。</p>
        <p>利益再分配（由私到公）内嵌的“逆向治理”形成机制。</p>
        <p>“双层利益再分配结构”如何推动乡村善治，及其内在逻辑和机制。</p>
        <p>6、研究方法与发现：</p>
        <p>
          通过深度剖析一个典型民族村的治理个案，发现项目利益分配私人化并未直接导致治理“寡头”。
        </p>
        <p>
          村庄权威精英与村干部既得利益群体博弈，创新利益再分配机制，构建了“逆向治理”模式。
        </p>
        <p>
          “双层利益再分配结构”模型有助于理解民族地区乡村“逆向治理”模式的生发逻辑，提振乡村社会参与基层治理的积极性，实现利益再分配和重塑村庄公共关系。
        </p>
        <p>
          透视了民族地区乡村“逆向治理”模式内蕴含的自治、法治、德治“三治融合”的乡村善治体系。
        </p>

        <p>二、“寡头治理”：项目进村初期利益分配失序表现</p>
        <p>
          在项目进村初期，Z镇X村出现了“寡头治理”现象，即利益分配失序。由于项目密集实施，X村成为利益聚集型村庄，村干部利用家族势力影响村庄选举，形成稳固的利益辐射圈。进村项目资源和政策导向家族化，导致公共资源分配差序化，村干部不仅差序分配项目，还与投标商建立“伙伴”关系，实现分利。这种利益分配私人化，虽然使村干部及其家族攫取了最大利益，但削弱了治理权威，加剧了村庄社会分化，冲击了治理传统，加剧了现实矛盾。
        </p>

        <p>三、“逆向治理”：项目利益再分配逻辑</p>
        <p>
          在项目利益分配私人化导致村干部与普通村民矛盾加剧的背景下，X村通过“逆向治理”实现了项目利益的再分配。这一过程中，集体主义文化的影响和约束起到了重要作用，它规定着村庄的行为限度，维系着村庄公共生活和村庄集体利益的公平分配。同时，村庄精英被激活并重新组织起来，建立了村集体利益共识机制和利益再分配规则。
        </p>
        <p>
          首先，集体主义文化对村庄社会的行为失范具有规范作用，它继续影响着村庄治理格局，并主导着绝大多数村民的行动逻辑。
        </p>
        <p>
          其次，村庄精英利用存量资源优势，组织村民与村干部进行利益博弈，使村干部让渡利益成为可能。这些精英具备崇高的威望，深得村民信任，是新一代乡贤。
        </p>
        <p>
          接着，为了达成利益再分配，X村建立了以村集体为依托的利益共识机制。村干部和村民都做出了妥协和让利，以换取未来集体土地的再分配资格和公共利益。
        </p>
        <p>
          最后，X村创新了利益再分配规则，制定了“抽成与分红”规则，并设立了专门的监督委员会进行有效监督。这一规则具有很高的内生性，较好调节了各利益主体的分歧，并赢得了所有规则接受者的赞同。
        </p>

        <p>四、通向善治：“双层利益再分配”新格局与乡村善治机制</p>

        <p>（一）构建双层利益再分配新格局</p>
        <p>主要阐述了在民族地区项目进村后，X村通过“逆向治理”实践，形成了村干部既得利益群体和普通村民的“双层利益再分配”结构和模式。这种分配模式是通过村干部和普通村民各自抽出部分利益作为集体利益，从而实现村庄利益关系的相对均衡。这种均衡的实现得益于村庄利益主体的分化、村庄互让伦理的制约以及村民普遍利益的满足。双层利益再分配新模式不仅遏制了项目利益私人化运作及其后果，还提高了村庄自治能力，推进了乡村善治的实现。</p>
        <p>（二）民族地区乡村善治机制创新</p>
        <p>主要讨论了双层利益再分配模式对于提升乡村治理水平和构建乡村善治机制的启发意义。首先，利益合理再分配机制是关键，通过集体主义的文化约束、村庄互让伦理制约、利益共识机制建立等，实现了利益的合理再分配。其次，村庄精英协调机制也至关重要，他们作为“中间人”在村民自治中发挥了重要作用，推动了村民自治运行和延续村庄善治理念。最后，制度保障机制是根本支撑，通过制度创新，如“抽成与分红”制度，稳定了利益再分配结构，并贯彻落实了利益合理再分配。</p>
        
        <p>五、结语</p>
        <p>指出民族地区提升乡村治理能力、推进乡村治理现代化建设是全面实施乡村振兴战略和走中国特色乡村善治的根本要求。X村的治理实践表明，要有效遏制村干部既得利益群体的利益分配私人化行为，必须利用民族地区的政策红利，处理好有效治理与高质量发展的关系，充分发挥村庄精英协调作用，激发村庄内生文化活力，创新利益再分配机制。同时，也指出了当前乡村治理中仍存在的问题，并提出了全方位建立健全乡村治理体系、提升乡村治理水平和治理能力的建议。这些建议包括全面实施乡村建设行动、政府积极嵌入现代化的乡村治理理念并规范自身行为、充分激发民族地区文化的内生活力等。</p>

